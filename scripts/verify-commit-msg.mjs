/**
 * Conventional Commits check, run from the `commit-msg` hook.
 *
 * Validates the header against:
 *   <type>[optional scope][!]: <description>
 *
 * A hand-rolled check rather than commitlint, to keep the toolchain to the two
 * dependencies this repo already leans on (Biome, tsc) instead of pulling in
 * commitlint plus its config package for one regex.
 *
 * Usage: node scripts/verify-commit-msg.mjs <path-to-COMMIT_EDITMSG>
 */
import { readFileSync } from "node:fs";

const TYPES = [
  "feat",
  "fix",
  "docs",
  "style",
  "refactor",
  "perf",
  "test",
  "build",
  "ci",
  "chore",
  "revert",
];

const MAX_HEADER = 100;

const path = process.argv[2];
if (!path) {
  console.error("verify-commit-msg: no commit message file given");
  process.exit(1);
}

const raw = readFileSync(path, "utf8");

// Drop comment lines git adds to the template, then find the first real line.
const lines = raw
  .split(/\r?\n/)
  .filter((line) => !line.startsWith("#"))
  .map((line) => line.trimEnd());
const header = lines.find((line) => line.trim().length > 0) ?? "";

// Merge commits and fixup/squash markers are generated by git — not ours to police.
if (/^(Merge|Revert)\s/.test(header) || /^(fixup|squash)!\s/.test(header)) {
  process.exit(0);
}

const pattern = new RegExp(
  `^(${TYPES.join("|")})(\\([a-z0-9._/-]+\\))?(!)?: .+`,
);

const problems = [];

if (!pattern.test(header)) {
  problems.push("header does not match `<type>(<scope>): <description>`");
}
if (header.length > MAX_HEADER) {
  problems.push(`header is ${header.length} chars, max is ${MAX_HEADER}`);
}
if (/\.$/.test(header)) {
  problems.push("header should not end with a period");
}

const description = header.split(": ").slice(1).join(": ");
if (description && /^[A-Z][a-z]/.test(description)) {
  problems.push("description should start lowercase");
}

// A blank line must separate header from body — git and most tools rely on it.
if (lines.length > 1 && lines[1] !== undefined && lines[1].trim() !== "") {
  problems.push("leave a blank line between the header and the body");
}

if (problems.length > 0) {
  console.error(`\n  Invalid commit message:\n\n    ${header}\n`);
  for (const problem of problems) {
    console.error(`  ✗ ${problem}`);
  }
  console.error(`\n  Allowed types: ${TYPES.join(", ")}`);
  console.error("  Example: feat(contact): send submissions through Resend\n");
  process.exit(1);
}
